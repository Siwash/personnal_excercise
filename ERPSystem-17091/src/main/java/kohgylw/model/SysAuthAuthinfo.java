package kohgylw.model;

public class SysAuthAuthinfo {
    private String authinfoId;

    private String authinfoName;

    private String authinfoStyle;

    private String authinfoUrl;

    public String getAuthinfoId() {
        return authinfoId;
    }

    public void setAuthinfoId(String authinfoId) {
        this.authinfoId = authinfoId;
    }

    public String getAuthinfoName() {
        return authinfoName;
    }

    public void setAuthinfoName(String authinfoName) {
        this.authinfoName = authinfoName;
    }

    public String getAuthinfoStyle() {
        return authinfoStyle;
    }

    public void setAuthinfoStyle(String authinfoStyle) {
        this.authinfoStyle = authinfoStyle;
    }

    public String getAuthinfoUrl() {
        return authinfoUrl;
    }

    public void setAuthinfoUrl(String authinfoUrl) {
        this.authinfoUrl = authinfoUrl;
    }
}