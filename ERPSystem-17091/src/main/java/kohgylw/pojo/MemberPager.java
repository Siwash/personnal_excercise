package kohgylw.pojo;

import java.util.List;

import kohgylw.model.SysMembermanageMemberinfo;

//辅助生成分页插件的
public class MemberPager {
	
	private int total;//总页码
	private int totalRecords;//总条数
	private List<SysMembermanageMemberinfo> memberinfos;//分页结果
	
	public int getTotal() {
		return total;
	}
	public void setTotal(int total) {
		this.total = total;
	}
	public int getTotalRecords() {
		return totalRecords;
	}
	public void setTotalRecords(int totalRecords) {
		this.totalRecords = totalRecords;
	}
	public List<SysMembermanageMemberinfo> getMemberinfos() {
		return memberinfos;
	}
	public void setMemberinfos(List<SysMembermanageMemberinfo> memberinfos) {
		this.memberinfos = memberinfos;
	}

}
